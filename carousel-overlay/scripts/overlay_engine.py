def load_captions(captions_path: str):
    """Dynamically load a captions file and return its FRAMES list."""
    spec   = importlib.util.spec_from_file_location("captions", captions_path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module.FRAMES


# ─── Font cache ──────────────────────────────────────────────────

_font_cache = {}

def get_font(style: str, stylized: bool = False) -> ImageFont.FreeTypeFont:
    cache_key = f"{style}_{'S' if stylized else 'P'}"
    if cache_key not in _font_cache:
        if stylized:
            path = FONT_STYLIZED
            size = FONT_SIZE.get("stylized", int(FONT_SIZE.get(style, 110) * 1.15))
        elif style == "cta":
            path = FONT_CTA
            size = FONT_SIZE.get("cta", 72)
        else:
            path = FONT_PRIMARY
            size = FONT_SIZE.get(style, 110)
        _font_cache[cache_key] = ImageFont.truetype(path, size)
    return _font_cache[cache_key]


# ─── Contrast methods ────────────────────────────────────────────

def apply_contrast(img: Image.Image, text: str, font, color_rgba: tuple,
                   x: int, y: int, method: str = None) -> Image.Image:
    """
    Draw text onto img using the specified contrast method.
    method: "stroke" | "dual_shadow" | "halo_shadow" | "stroke_shadow"
    Falls back to TEXT_CONTRAST["method"] if None.
    Returns the composited image.
    """
    cfg    = TEXT_CONTRAST
    method = method or cfg["method"]
    out    = img.convert("RGBA")

    if method == "stroke":
        d = ImageDraw.Draw(out)
        d.text((x, y), text, font=font, fill=color_rgba,
               stroke_width=cfg["stroke_width"],
               stroke_fill=cfg["stroke_color"])

    elif method == "dual_shadow":
        # Wide soft shadow
        wide = Image.new("RGBA", out.size, (0, 0, 0, 0))
        wd   = ImageDraw.Draw(wide)
        off  = cfg["shadow_wide_offset"]
        wd.text((x + off, y + off), text, font=font, fill=(0, 0, 0, cfg["shadow_wide_alpha"]))
        wide = wide.filter(ImageFilter.GaussianBlur(radius=cfg["shadow_wide_blur"]))
        out  = Image.alpha_composite(out, wide)
        # Tight hard shadow
        tight = Image.new("RGBA", out.size, (0, 0, 0, 0))
        td    = ImageDraw.Draw(tight)
        off2  = cfg["shadow_tight_offset"]
        td.text((x + off2, y + off2), text, font=font, fill=(0, 0, 0, cfg["shadow_tight_alpha"]))
        out = Image.alpha_composite(out, tight)
        d   = ImageDraw.Draw(out)
        d.text((x, y), text, font=font, fill=color_rgba)

    elif method == "halo_shadow":
        # Blurred halo — wraps letterforms in a glow
        halo = Image.new("RGBA", out.size, (0, 0, 0, 0))
        hd   = ImageDraw.Draw(halo)
        hd.text((x, y), text, font=font, fill=(0, 0, 0, cfg["halo_alpha"]))
        halo = halo.filter(ImageFilter.GaussianBlur(radius=cfg["halo_blur"]))
        out  = Image.alpha_composite(out, halo)
        # Tight shadow underneath for edge definition
        tight = Image.new("RGBA", out.size, (0, 0, 0, 0))
        td    = ImageDraw.Draw(tight)
        off   = cfg["shadow_tight_offset"]
        td.text((x + off, y + off), text, font=font, fill=(0, 0, 0, cfg["shadow_tight_alpha"]))
        out = Image.alpha_composite(out, tight)
        d   = ImageDraw.Draw(out)
        d.text((x, y), text, font=font, fill=color_rgba)

    elif method == "stroke_shadow":
        # Soft shadow + thin stroke — hybrid
        shadow = Image.new("RGBA", out.size, (0, 0, 0, 0))
        sd     = ImageDraw.Draw(shadow)
        off    = cfg["shadow_wide_offset"]
        sd.text((x + off, y + off), text, font=font, fill=(0, 0, 0, 160))
        shadow = shadow.filter(ImageFilter.GaussianBlur(radius=14))
        out    = Image.alpha_composite(out, shadow)
        d      = ImageDraw.Draw(out)
        d.text((x, y), text, font=font, fill=color_rgba,
               stroke_width=max(3, cfg["stroke_width"] // 2),
               stroke_fill=cfg["stroke_color"])

    else:
        # Unknown method — fall back to stroke
        d = ImageDraw.Draw(out)
        d.text((x, y), text, font=font, fill=color_rgba,
               stroke_width=cfg["stroke_width"],
               stroke_fill=cfg["stroke_color"])

    return out


# ─── Scrim drawing ───────────────────────────────────────────────

def draw_scrim(img: Image.Image, side: str, height_pct: float, opacity_max: int) -> Image.Image:
    """Draw a gradient scrim on a copy of the image."""
    w, h = img.size
    scrim_h = int(h * height_pct)

    scrim = Image.new("RGBA", (w, scrim_h), (0, 0, 0, 0))
    draw = ImageDraw.Draw(scrim)

    for i in range(scrim_h):
        if side == "top":
            # Darkest at top, fades down
            alpha = int(opacity_max * (1 - i / scrim_h))
        else:
            # Darkest at bottom, fades up
            alpha = int(opacity_max * (i / scrim_h))
        draw.line([(0, i), (w, i)], fill=(0, 0, 0, alpha))

    result = img.convert("RGBA")
    if side == "top":
        result.paste(scrim, (0, 0), scrim)
    else:
        result.paste(scrim, (0, h - scrim_h), scrim)

    return result


# ─── Text drawing ────────────────────────────────────────────────

def wrap_text(text: str, max_chars: int) -> list[str]:
    """Wrap text at word boundaries."""
    return textwrap.wrap(text, width=max_chars)


def measure_text_block(lines: list[str], font: ImageFont.FreeTypeFont, spacing: int) -> tuple[int, int]:
    """Return (width, height) of a multi-line text block."""
    dummy = Image.new("RGBA", (10, 10))
    draw = ImageDraw.Draw(dummy)
    max_w = 0
    total_h = 0
    for i, line in enumerate(lines):
        bbox = draw.textbbox((0, 0), line, font=font)
        lw = bbox[2] - bbox[0]
        lh = bbox[3] - bbox[1]
        max_w = max(max_w, lw)
        total_h += lh
        if i < len(lines) - 1:
            total_h += spacing
    return max_w, total_h


def split_text_emoji(text: str):
    """Split text into (segment, is_emoji) pairs for mixed rendering."""
    if not EMOJI_AVAILABLE:
        return [(text, False)]
    result = []
    current = ""
    for char in text:
        if emoji_lib.is_emoji(char):
            if current:
                result.append((current, False))
                current = ""
            result.append((char, True))
        else:
            current += char
    if current:
        result.append((current, False))
    return result


def measure_mixed_line(text: str, font: ImageFont.FreeTypeFont, emoji_font) -> tuple[int, int]:
    """Measure width and height of a line with mixed text/emoji."""
    dummy = Image.new("RGBA", (10, 10))
    draw = ImageDraw.Draw(dummy)
    total_w = 0
    max_h = 0
    for segment, is_emoji in split_text_emoji(text):
        if not segment.strip() and is_emoji is False:
            # Space
            bbox = draw.textbbox((0, 0), segment, font=font)
            total_w += bbox[2] - bbox[0]
            max_h = max(max_h, bbox[3] - bbox[1])
        elif is_emoji and emoji_font:
            bbox = draw.textbbox((0, 0), segment, font=emoji_font)
            total_w += bbox[2] - bbox[0]
            max_h = max(max_h, bbox[3] - bbox[1])
        else:
            clean = emoji_lib.replace_emoji(segment, replace='') if EMOJI_AVAILABLE else segment
            if clean:
                bbox = draw.textbbox((0, 0), clean, font=font)
                total_w += bbox[2] - bbox[0]
                max_h = max(max_h, bbox[3] - bbox[1])
    return total_w, max_h


def draw_text_block(
    draw: ImageDraw.ImageDraw,
    lines: list[str],
    font: ImageFont.FreeTypeFont,
    color_rgba: tuple,
    img_size: tuple[int, int],
    position: str,
    spacing: int,
    padding_x: int,
    padding_y: int,
    img_ref: Image.Image = None,
):
    """Draw centered multi-line text anchored to top or bottom, with emoji support."""
    w, h = img_size
    shadow_offset = max(3, font.size // 30)

    # Load emoji font if available
    emoji_font = None
    if EMOJI_AVAILABLE and os.path.exists(EMOJI_FONT_PATH):
        try:
            emoji_font = ImageFont.truetype(EMOJI_FONT_PATH, int(font.size * 0.85))
        except Exception:
            pass

    # Measure total block height
    line_heights = []
    for line in lines:
        if EMOJI_AVAILABLE and emoji_font:
            _, lh = measure_mixed_line(line, font, emoji_font)
        else:
            bbox = draw.textbbox((0, 0), line, font=font)
            lh = bbox[3] - bbox[1]
        line_heights.append(max(lh, font.size))

    block_h = sum(line_heights) + spacing * (len(lines) - 1)

    if position == "top":
        y_start = padding_y
    else:
        y_start = h - padding_y - block_h

    for li, line in enumerate(lines):
        lh = line_heights[li]

        if EMOJI_AVAILABLE and emoji_font:
            # Measure full line width for centering
            line_w, _ = measure_mixed_line(line, font, emoji_font)
        else:
            clean_line = emoji_lib.replace_emoji(line, replace='').strip() if EMOJI_AVAILABLE else line
            bbox = draw.textbbox((0, 0), clean_line, font=font)
            line_w = bbox[2] - bbox[0]

        x_cursor = (w - line_w) // 2

        # Render each segment
        for segment, is_emoji in split_text_emoji(line):
            if is_emoji and emoji_font and img_ref is not None:
                # Render emoji on a temp surface and paste (color emoji needs RGBA composite)
                bbox = draw.textbbox((0, 0), segment, font=emoji_font)
                seg_w = bbox[2] - bbox[0]
                seg_h = bbox[3] - bbox[1]
                emoji_surface = Image.new("RGBA", (seg_w + 10, lh + 10), (0, 0, 0, 0))
                ed = ImageDraw.Draw(emoji_surface)
                ed.text((5, 5), segment, font=emoji_font, embedded_color=True)
                # Paste onto image
                paste_x = x_cursor
                paste_y = y_start + (lh - seg_h) // 2
                img_ref.paste(emoji_surface, (paste_x, paste_y), emoji_surface)
                x_cursor += seg_w
            else:
                clean_seg = emoji_lib.replace_emoji(segment, replace='').strip() if EMOJI_AVAILABLE else segment
                if not clean_seg:
                    # Just spaces
                    if segment:
                        bbox = draw.textbbox((0, 0), segment, font=font)
                        x_cursor += bbox[2] - bbox[0]
                    continue
                bbox = draw.textbbox((0, 0), clean_seg, font=font)
                seg_w = bbox[2] - bbox[0]
                seg_h = bbox[3] - bbox[1]
                y_text = y_start + (lh - seg_h) // 2
                # Shadow
                draw.text((x_cursor + shadow_offset, y_text + shadow_offset),
                          clean_seg, font=font, fill=(0, 0, 0, 160))
                draw.text((x_cursor, y_text), clean_seg, font=font, fill=color_rgba)
                x_cursor += seg_w

        y_start += lh + spacing


