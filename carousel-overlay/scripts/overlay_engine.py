def draw_text_block_safe(
    draw: ImageDraw.ImageDraw,
    lines: list[str],
    font: ImageFont.FreeTypeFont,
    color_rgba: tuple,
    safe_bounds: tuple[int, int, int, int],
    img_size: tuple[int, int],
    position: str,
    spacing: int,
    padding_x: int,
    padding_y: int,
    img_ref: Image.Image = None,
    stylized: bool = False,
    contrast: str = None,
    font_path: str = None,   # explicit path used for shrink loop — overrides auto-detection
):
    """
    Safe-zone-aware text renderer with subtitle stability + reading UX.

    READING UX RULES APPLIED:
    - Fixed Y anchors: top/bottom overlays always land at the same Y every frame.
      Reader's eye parks there after frame 2 and stops hunting.
    - 60% line width cap: keeps lines to 1-2 eye fixations on mobile.
    - Auto-shrink: font reduces until all lines fit within width budget.
    - Hard 2-line cap from LAYOUT["max_lines"].
    - Stylized/middle frames bypass anchors — editorial rules apply instead.
    """
    safe_x1, safe_y1, safe_x2, safe_y2 = safe_bounds
    frame_w, frame_h = img_size

    stroke_pad = TEXT_CONTRAST.get("stroke_width", 7) + 4

    # Width budget: 60% of frame for regular text, full safe width for stylized
    if stylized or position == "middle":
        max_line_px = (safe_x2 - safe_x1) - (stroke_pad * 2)
    else:
        max_line_px = int(frame_w * LAYOUT.get("line_width_pct", 0.60)) - (stroke_pad * 2)

    emoji_font = None
    if EMOJI_AVAILABLE and os.path.exists(EMOJI_FONT_PATH):
        try:
            emoji_font = ImageFont.truetype(EMOJI_FONT_PATH, int(font.size * 0.85))
        except Exception:
            pass

    def measure_line(line, fnt):
        if EMOJI_AVAILABLE and emoji_font:
            lw, lh = measure_mixed_line(line, fnt, emoji_font)
        else:
            clean = emoji_lib.replace_emoji(line, replace='').strip() if EMOJI_AVAILABLE else line
            d = ImageDraw.Draw(Image.new("RGBA", (10, 10)))
            bb = d.textbbox((0, 0), clean, font=fnt)
            lw, lh = bb[2] - bb[0], bb[3] - bb[1]
        return lw, max(lh, fnt.size)

    # Re-wrap lines to fit within pixel width budget at the current font size.
    # Font size never changes — only line breaks do.
    def wrap_to_pixel_width(text_lines, fnt, budget_px):
        """Re-wrap already-split lines so no rendered line exceeds budget_px."""
        result = []
        for line in text_lines:
            words = line.split()
            if not words:
                continue
            current = words[0]
            for word in words[1:]:
                trial = current + " " + word
                lw, _ = measure_line(trial, fnt)
                if lw <= budget_px:
                    current = trial
                else:
                    result.append(current)
                    current = word
            result.append(current)
        return result

    # Step 1: wrap to pixel budget at full font size
    lines = wrap_to_pixel_width(lines, font, max_line_px)

    # Step 2: if wrapping still gives 3+ lines, shrink font until ≤ 2 lines
    if len(lines) > LAYOUT.get("max_lines", 2):
        current_font = font
        # Use explicit path if provided, otherwise auto-detect
        shrink_path = font_path or (FONT_STYLIZED if stylized else FONT_PRIMARY)
        for _ in range(20):
            candidate = wrap_to_pixel_width(
                [" ".join(lines)], current_font, max_line_px
            )
            if len(candidate) <= LAYOUT.get("max_lines", 2):
                font  = current_font
                lines = candidate
                break
            new_size = max(40, int(current_font.size * 0.92))
            if new_size == current_font.size:
                lines = lines[:LAYOUT.get("max_lines", 2)]
                break
            try:
                current_font = ImageFont.truetype(shrink_path, new_size)
            except Exception:
                break

    # Measure final block
    line_heights, line_widths = [], []
    for line in lines:
        lw, lh = measure_line(line, font)
        line_heights.append(lh)
        line_widths.append(lw)

    block_h = sum(line_heights) + spacing * (len(lines) - 1)

    # Fixed Y anchors — subtitle stability
    # top/bottom use absolute frame-% anchors so position is identical every frame.
    # middle/stylized use editorial 58% placement within safe zone.
    if position == "middle" or stylized:
        safe_h  = safe_y2 - safe_y1
        y_start = safe_y1 + int(safe_h * 0.58) - block_h // 2
    elif position == "top":
        y_start = int(frame_h * LAYOUT.get("anchor_top_pct", 0.12))
    else:  # bottom
        y_start = int(frame_h * LAYOUT.get("anchor_bottom_pct", 0.76)) - block_h

    # Draw lines
    for li, line in enumerate(lines):
        lh     = line_heights[li]
        line_w = line_widths[li]
        x      = (frame_w - line_w) // 2   # center on full frame width

        clean_line = emoji_lib.replace_emoji(line, replace='').strip() if EMOJI_AVAILABLE else line

        if clean_line:
            img_ref = apply_contrast(
                img_ref, clean_line, font, color_rgba,
                x=x, y=y_start, method=contrast
            )
            draw = ImageDraw.Draw(img_ref)

        if EMOJI_AVAILABLE and emoji_font:
            ex = x
            for segment, is_emoji in split_text_emoji(line):
                if is_emoji and img_ref is not None:
                    bb = draw.textbbox((0, 0), segment, font=emoji_font)
                    sw, sh = bb[2] - bb[0], bb[3] - bb[1]
                    es = Image.new("RGBA", (sw + 10, lh + 10), (0, 0, 0, 0))
                    ed = ImageDraw.Draw(es)
                    ed.text((5, 5), segment, font=emoji_font, embedded_color=True)
                    img_ref.paste(es, (ex + line_w - sw, y_start + (lh - sh) // 2), es)
                    draw = ImageDraw.Draw(img_ref)
                else:
                    cs = emoji_lib.replace_emoji(segment, replace='').strip() if EMOJI_AVAILABLE else segment
                    if cs:
                        bb = draw.textbbox((0, 0), cs, font=font)
                        ex += bb[2] - bb[0]

        y_start += lh + spacing

    return img_ref




def draw_safe_zone_guide(img: Image.Image) -> Image.Image:
    """Draw a visible safe zone border for QA. Controlled by SHOW_SAFE_ZONE_GUIDE in stylesheet."""
    w, h = img.size
    overlay = img.copy().convert("RGBA")
    draw = ImageDraw.Draw(overlay, "RGBA")

    sz = IG_SAFE
    x1 = int(w * sz["left_clear_pct"])
    y1 = int(h * sz["top_clear_pct"])
    x2 = int(w * (1 - sz["right_clear_pct"]))
    y2 = int(h * (1 - sz["bottom_clear_pct"]))

    # Danger zones — red tint
    draw.rectangle([0,  0,  w,  y1], fill=(220, 40, 40, 80))   # top
    draw.rectangle([0,  y2, w,  h],  fill=(220, 40, 40, 80))   # bottom
    draw.rectangle([x2, y1, w,  y2], fill=(220, 120, 40, 80))  # right buttons

    # Safe zone border — green dashed feel via thin rect
    draw.rectangle([x1, y1, x2, y2], outline=(80, 220, 120, 200), width=6)

    # Labels
    font_path = FONT_PRIMARY
    try:
        lbl_font = ImageFont.truetype(font_path, 44)
        draw.text((x1 + 20, y1 + 20), "✓ IG SAFE ZONE", font=lbl_font, fill=(80, 220, 120, 230))
        draw.text((w//2 - 200, 30), "▲ PROFILE BAR", font=lbl_font, fill=(255, 255, 255, 200))
        draw.text((w//2 - 250, y2 + 30), "▼ CAPTION / LIKES / NAV", font=lbl_font, fill=(255, 255, 255, 200))
        draw.text((x2 + 10, h//2 - 80), "◀ BTNS", font=lbl_font, fill=(255, 200, 100, 200))
    except Exception:
        pass

    return Image.alpha_composite(img.convert("RGBA"), overlay.convert("RGBA"))


def render_frame(frame_config: dict, input_dir: str, output_dir: str):
    filename = frame_config["file"]
    overlays = frame_config.get("overlays", [])

    src_path = os.path.join(input_dir, filename)
    if not os.path.exists(src_path):
        print(f"  ⚠  Skipping {filename} — not found in {input_dir}")
        return

    img = Image.open(src_path).convert("RGBA")
    w, h = img.size

    # ── IG Safe zone bounds ──────────────────────────────────────
    sz = IG_SAFE
    safe_x1   = int(w * sz["left_clear_pct"])
    safe_y1   = int(h * sz["top_clear_pct"])
    safe_x2   = int(w * (1 - sz["right_clear_pct"]))
    safe_y2   = int(h * (1 - sz["bottom_clear_pct"]))
    safe_w    = safe_x2 - safe_x1

    # Inner padding within safe zone
    padding_x = int(w * LAYOUT["padding_x_pct"])
    padding_y = int(h * LAYOUT["padding_y_pct"])

    for overlay in overlays:
        if overlay.get("scrim", False):
            img = draw_scrim(
                img,
                side=overlay.get("scrim_side", "bottom"),
                height_pct=SCRIM["height_pct"],
                opacity_max=SCRIM["opacity_max"],
            )

        stylized  = overlay.get("stylized", False)
        contrast  = overlay.get("contrast", None)

        # Per-overlay font path override (e.g. Oi for frame 10 closer)
        font_override = overlay.get("font", None)
        if font_override:
            size = FONT_SIZE.get(overlay["style"], 110)
            try:
                font = ImageFont.truetype(font_override, size)
            except Exception:
                font = get_font(overlay["style"], stylized=stylized)
        else:
            font = get_font(overlay["style"], stylized=stylized)

        color = COLOR[overlay["color"]]
        text  = overlay["text"]
        pos   = overlay["position"]

        lines = wrap_text(text, LAYOUT["max_chars"])
        draw  = ImageDraw.Draw(img)

        img = draw_text_block_safe(
            draw=draw,
            lines=lines,
            font=font,
            color_rgba=color,
            safe_bounds=(safe_x1, safe_y1, safe_x2, safe_y2),
            img_size=(w, h),
            position=pos,
            spacing=LAYOUT["line_spacing"],
            padding_x=padding_x,
            padding_y=padding_y,
            img_ref=img,
            stylized=stylized,
            contrast=contrast,
            font_path=font_override,
        ) or img

    # Optional QA guide
    if SHOW_SAFE_ZONE_GUIDE:
        img = draw_safe_zone_guide(img)

    name, ext = os.path.splitext(filename)
    out_name = f"{name}{OUTPUT['suffix']}.png"
    out_path = os.path.join(output_dir, out_name)
    img.convert("RGB").save(out_path, format=OUTPUT["format"])
    print(f"  ✓  {filename}  →  {out_name}")


def render_carousel(input_dir: str, output_dir: str, captions_path: str = None):
    global FRAMES

    # Resolve captions — explicit path, or default location
    if captions_path:
        frames = load_captions(captions_path)
    else:
        # Try assets/captions/default.py, then fall back to legacy captions.py
        default_paths = [
            os.path.join(_ASSETS_DIR, "captions", "default.py"),
            os.path.join(_SCRIPT_DIR, "captions.py"),
        ]
        frames = None
        for p in default_paths:
            if os.path.exists(p):
                frames = load_captions(p)
                break
        if frames is None:
            print("Error: no captions file found. Pass --captions path/to/captions.py")
            sys.exit(1)

    FRAMES = frames
    os.makedirs(output_dir, exist_ok=True)

    print(f"\nCarousel Overlay Engine")
    print(f"Input:    {input_dir}")
    print(f"Output:   {output_dir}")
    print(f"Captions: {captions_path or '(default)'}")
    print(f"Frames:   {len(FRAMES)}\n")

    for frame in FRAMES:
        render_frame(frame, input_dir, output_dir)

    print(f"\nDone. {len(FRAMES)} frames rendered → {output_dir}\n")


# ─── CLI ─────────────────────────────────────────────────────────

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Carousel Overlay Engine")
    parser.add_argument("--input",    default=".",        help="Directory containing source images")
    parser.add_argument("--output",   default="./output", help="Directory for rendered frames")
    parser.add_argument("--captions", default=None,       help="Path to captions .py file")
    args = parser.parse_args()
    render_carousel(args.input, args.output, args.captions)

